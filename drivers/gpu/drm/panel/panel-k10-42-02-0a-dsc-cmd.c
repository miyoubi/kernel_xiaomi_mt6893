int panel_set_doze_brightness(struct drm_panel *panel, int doze_brightness)
{
	struct lcm *ctx;
	int fps;
	int ret;

	char fps_tb[] = {0x2F, 0x01};
	char fps_irdrop_page_tb[] = {0xF0, 0x55, 0xAA, 0x52, 0x08, 0x01};
	char fps_irdrop_value_tb[] = {0xC3, 0x8C, 0x01, 0x93, 0xD0, 0x20, 0x81, 0x00};
	char page1_tb[] = {0xF0, 0x55, 0xAA, 0x52, 0x08, 0x01};
	char page1_offset_aod_tb[] = {0xCE, 0x04};
	char page_tb[] = {0xF0, 0x55, 0xAA, 0x52, 0x08, 0x00};
	char page_offset_aod_tb[] = {0xB2, 0x18};
	char page_offset_aod_value_tb[] = {0x39};
	char page_aod_value_tb[] = {0x65, 0x01};
	char aod_value_common_tb[] = {0x2C};
	char bl_tb[] = {0x51, 0x07, 0xFF, 0x0F, 0xFF};

	if (!panel) {
		pr_err("invalid params\n");
		return -EAGAIN;
	}

	ctx = panel_to_lcm(panel);

	if (ctx->doze_brightness_state == doze_brightness)
		return -EAGAIN;

	pr_info("%s+ doze_brightness:%d\n", __func__, doze_brightness);

	if (DOZE_BRIGHTNESS_HBM == doze_brightness) {
		bl_tb[3] = 0x0F;
		bl_tb[4] = 0xFF;
	}
	else if(DOZE_BRIGHTNESS_LBM == doze_brightness) {
		bl_tb[3] = 0x01;
		bl_tb[4] = 0x55;
	}
	else {
		page_offset_aod_tb[1] = 0x98;
		page1_offset_aod_tb[1] = 0x0C;
		page_offset_aod_value_tb[0] = 0x38;
		page_aod_value_tb[1] = 0x00;
		bl_tb[1] = bl_tb0[1];
		bl_tb[2] = bl_tb0[2];
	}

	if ((DOZE_BRIGHTNESS_HBM == doze_brightness) || (DOZE_BRIGHTNESS_LBM == doze_brightness)) {
		lcm_dcs_write(ctx, fps_tb, ARRAY_SIZE(fps_tb));
		lcm_dcs_write(ctx, fps_irdrop_page_tb, ARRAY_SIZE(fps_irdrop_page_tb));
		lcm_dcs_write(ctx, fps_irdrop_value_tb, ARRAY_SIZE(fps_irdrop_value_tb));
		lcm_dcs_write(ctx, page1_tb, ARRAY_SIZE(page1_tb));
		lcm_dcs_write(ctx, page1_offset_aod_tb, ARRAY_SIZE(page1_offset_aod_tb));
		lcm_dcs_write(ctx, page_tb, ARRAY_SIZE(page_tb));
		lcm_dcs_write(ctx, page_offset_aod_tb, ARRAY_SIZE(page_offset_aod_tb));
		lcm_dcs_write(ctx, page_offset_aod_value_tb, ARRAY_SIZE(page_offset_aod_value_tb));
		lcm_dcs_write(ctx, page_aod_value_tb, ARRAY_SIZE(page_aod_value_tb));
		lcm_dcs_write(ctx, aod_value_common_tb, ARRAY_SIZE(aod_value_common_tb));
		lcm_dcs_write(ctx, bl_tb, ARRAY_SIZE(bl_tb));
	} else {
		lcm_dcs_write(ctx, page_tb, ARRAY_SIZE(page_tb));
		lcm_dcs_write(ctx, page_offset_aod_tb, ARRAY_SIZE(page_offset_aod_tb));
		lcm_dcs_write(ctx, page_aod_value_tb, ARRAY_SIZE(page_aod_value_tb));
		lcm_dcs_write(ctx, page_offset_aod_value_tb, ARRAY_SIZE(page_offset_aod_value_tb));
		lcm_dcs_write(ctx, aod_value_common_tb, ARRAY_SIZE(aod_value_common_tb));
		lcm_dcs_write(ctx, page1_tb, ARRAY_SIZE(page1_tb));
		lcm_dcs_write(ctx, page1_offset_aod_tb, ARRAY_SIZE(page1_offset_aod_tb));
		lcm_dcs_write(ctx, fps_tb, ARRAY_SIZE(fps_tb));
		lcm_dcs_write(ctx, bl_tb, ARRAY_SIZE(bl_tb));
	}
	ctx->doze_brightness_state = doze_brightness;

	if ((DOZE_BRIGHTNESS_HBM == doze_brightness) || (DOZE_BRIGHTNESS_LBM == doze_brightness))
		fps = 30;
	else
		fps = ctx->dynamic_fps;

	g_notify_data.disp_id = MI_DISPLAY_PRIMARY;
	g_notify_data.data = &fps;
	mi_disp_notifier_call_chain(MI_DISP_CHANGE_FPS, &g_notify_data);

	pr_info("%s-\n", __func__);

	return ret;
}

