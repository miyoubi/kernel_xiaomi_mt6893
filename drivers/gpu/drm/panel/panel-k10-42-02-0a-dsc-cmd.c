static void lcm_dcs_write(struct lcm *ctx, const void *data, size_t len)
{
	struct mipi_dsi_device *dsi = to_mipi_dsi_device(ctx->dev);
	ssize_t ret;
	char *addr;

	if (ctx->error < 0)
		return;

	addr = (char *)data;
	if ((int)*addr < 0xB0)
		ret = mipi_dsi_dcs_write_buffer(dsi, data, len);
	else
		ret = mipi_dsi_generic_write(dsi, data, len);
	if (ret < 0) {
		dev_err(ctx->dev, "error %zd writing seq: %ph\n", ret, data);
		ctx->error = ret;
	}
}

static void push_table(struct lcm *ctx, struct LCM_setting_table *table, unsigned int count)
{
	unsigned int i,j;
	unsigned char temp[30] = {0};
	for (i = 0; i < count; i++) {
		unsigned cmd;
		cmd = table[i].cmd;
		memset(temp, 0, sizeof(temp));
		switch (cmd) {
			case REGFLAG_DELAY:
				if (table[i].count <= 10)
					msleep(table[i].count);
				else
					msleep(table[i].count);
				break;
			case REGFLAG_END_OF_TABLE:
				break;
			default:
				temp[0] = cmd;
				for (j = 0; j < table[i].count; j++ ) {
					temp[j+1] = table[i].para_list[j];
				}
				lcm_dcs_write(ctx, temp, table[i].count+1);
		}
	}
}
static void lcm_panel_init(struct lcm *ctx)
{
	pr_info("%s+\n", __func__);
	gpiod_set_value(ctx->reset_gpio, 0);
	msleep(15);
	gpiod_set_value(ctx->reset_gpio, 1);
	msleep(1);
	gpiod_set_value(ctx->reset_gpio, 0);
	msleep(10);
	gpiod_set_value(ctx->reset_gpio, 1);
	msleep(10);
	push_table(ctx, init_setting, sizeof(init_setting) / sizeof(struct LCM_setting_table));
	pr_info("%s-\n", __func__);
}

static int lcm_disable(struct drm_panel *panel)
{
	struct lcm *ctx = panel_to_lcm(panel);

	if (!ctx->enabled)
		return 0;

	if (ctx->backlight) {
		ctx->backlight->props.power = FB_BLANK_POWERDOWN;
		backlight_update_status(ctx->backlight);
	}

	ctx->enabled = false;

	return 0;
}

static int lcm_unprepare(struct drm_panel *panel)
{
	struct lcm *ctx = panel_to_lcm(panel);
	int ret;
	pr_info("%s+\n", __func__);
	if (!ctx->prepared)
		return 0;

	lcm_dcs_write_seq_static(ctx, 0x28);
	usleep_range(10 * 1000, 10 * 1000 + 10);
	lcm_dcs_write_seq_static(ctx, 0x10);
	usleep_range(120 * 1000, 120 * 1000 + 10);

	gpiod_set_value(ctx->reset_gpio, 0);
	usleep_range(10, 10 + 10);

	/*Power off VCI_3P0*/
	pmic_set_register_value(PMIC_RG_VIBR_VOSEL, 0xB);
	pmic_set_register_value(PMIC_RG_LDO_VIBR_EN, 0);

	/*disable regulator*/
	/*wl2866d0-dvdd2 regulator enable before disable*/
	if (0 == wl2866d0_dvdd2_off) {
		pr_info("we first enable regulator before disable wl2866d0-dvdd2 in the first time, %s\n", __func__);
		ret = regulator_enable(disp_dvdd);
		if (ret < 0)
			pr_err("enable regulator disp_dvdd fail in the first time, ret = %d\n", ret);
		wl2866d0_dvdd2_off = 1;
	}
	ret = regulator_disable(disp_dvdd);
	if (ret < 0)
		pr_err("disable regulator disp_dvdd fail, ret = %d\n", ret);

	pmic_set_register_value(PMIC_RG_VM18_VOSEL, 0x0C);
	pmic_set_register_value(PMIC_RG_LDO_VM18_EN, 0);

	ctx->error = 0;
	ctx->prepared = false;

	pr_info("%s-\n", __func__);
	return 0;
}

static int lcm_prepare(struct drm_panel *panel)
{
	struct lcm *ctx = panel_to_lcm(panel);
	int ret;

	pr_info("%s+\n", __func__);
	if (ctx->prepared)
		return 0;

        /*OLED_VDDI_VM18_EN*/
	pmic_set_register_value(PMIC_RG_VM18_VOSEL, 0x0C);
	pmic_set_register_value(PMIC_RG_LDO_VM18_EN, 1);
	usleep_range(20 * 1000, 20 * 1000 + 10);

	/*OLED_DVDD_1P2_EN*/
	/*enable regulator*/
	ret = regulator_enable(disp_dvdd);
	if (ret < 0)
		pr_err("enable regulator disp_dvdd fail, ret = %d\n", ret);
	usleep_range(5 * 1000, 5 * 1000 + 10);

	/*Power on VCI_3P0*/
	pmic_set_register_value(PMIC_RG_VIBR_VOSEL, 0xB);
	pmic_set_register_value(PMIC_RG_LDO_VIBR_EN, 1);

	lcm_panel_init(ctx);

	ret = ctx->error;
	if (ret < 0)
		lcm_unprepare(panel);

	ctx->prepared = true;

#ifdef PANEL_SUPPORT_READBACK
	lcm_panel_get_data(ctx);
#endif

	pr_info("%s-\n", __func__);
	return ret;
}

